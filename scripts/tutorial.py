model1 = torch.load(path1)
model2 = torch.load(path2)
p = ot.unif(len(model1))
q = ot.unif(len(model2))
#　%%
# 2023.4.3 佐々木　
# 阿部さんが作った、以下のオリジナルのdirectory構造の方が良さそうです (特に2条件以上で計算を行う場合)。
